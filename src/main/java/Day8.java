    static int solve(final String input) {
        int highestValueEver = 0;
        final Map<String, Integer> register = new HashMap<>();
        for (final Operation operation : getOperations(input)) {
            final String targetRegister = operation.getTargetRegister();
            // init if missing
            if (!register.containsKey(targetRegister)) {
                register.put(targetRegister, 0);
            }
            final String conditionRegister = operation.getConditionRegister();
            if (!register.containsKey(conditionRegister)) {
                register.put(conditionRegister, 0);
            }
            final boolean matchesCondition = matchesCondition(operation, register.get(conditionRegister));
            if (matchesCondition) {
                final Integer previousValue = register.get(operation.getTargetRegister());
                final Integer newValue = previousValue + (operation.getSign() * operation.getModifier());
                if (newValue > highestValueEver) {
                    highestValueEver = newValue;
                }
                register.put(operation.getTargetRegister(), newValue);
            }
        }

        int maxValue = 0;
        for (final Entry<String, Integer> entry : register.entrySet()) {
            if (entry.getValue() > maxValue) {
                maxValue = entry.getValue();
            }
        }

        System.out.println("2: " + highestValueEver);
        return maxValue;
    }

    private static boolean matchesCondition(final Operation operation, final Integer conditionRegister) {
        final String operator = operation.getConditionOperator();
        switch (operator) {
            case "!=":
                return !conditionRegister.equals(operation.getConditionValue());
            case "==":
                return conditionRegister.equals(operation.getConditionValue());
            case "<":
                return conditionRegister < operation.getConditionValue();
            case "<=":
                return conditionRegister <= operation.getConditionValue();
            case ">":
                return conditionRegister > operation.getConditionValue();
            case ">=":
                return conditionRegister >= operation.getConditionValue();
            default:
                throw new RuntimeException("Unknown operator: " + operator);
        }
    }

    private static List<Operation> getOperations(final String input) {
        return Arrays.stream(input.split("\n")).map(Day8::toOperation).collect(Collectors.toList());
    }

    private static Operation toOperation(final String line) {
        final String[] split = line.split(" ");
        return new Operation(split[0], getSign(split[1]), Integer.parseInt(split[2]), split[4], split[5], Integer.parseInt(split[6]));
    }

    private static Byte getSign(final String s) {
        if (s.equals("inc")) {
            return 1;
        } else if (s.equals("dec")) {
            return -1;
        } else {
            throw new RuntimeException("Unknown sign: " + s);
        }
    }

